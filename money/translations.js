function detectLanguage() {
    // Check URL parameter first
    const urlParams = new URLSearchParams(window.location.search);
    const urlLang = urlParams.get('lang');
    if (urlLang && translations[urlLang]) return urlLang;

    // Check localStorage
    const savedLang = localStorage.getItem('cro-sniper-lang');
    if (savedLang && translations[savedLang]) return savedLang;

    // Detect browser language
    const browserLang = navigator.language.split('-')[0];
    if (translations[browserLang]) return browserLang;

    // Default: Spanish
    return 'es';
}

function setLanguage(lang) {
    if (!translations[lang]) return;

    localStorage.setItem('cro-sniper-lang', lang);
    document.documentElement.lang = lang;

    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            // Use innerHTML for elements that contain HTML (spans, etc)
            if (translations[lang][key].includes('<')) {
                el.innerHTML = translations[lang][key];
            } else {
                el.textContent = translations[lang][key];
            }
        }
    });

    // Update feature lists (pipe-separated)
    document.querySelectorAll('[data-i18n-features]').forEach(el => {
        const key = el.getAttribute('data-i18n-features');
        if (translations[lang][key]) {
            const features = translations[lang][key].split('|');
            el.innerHTML = features.map(f => `<li><svg width="16" height="16" viewBox="0 0 16 16" fill="none"><path d="M13.5 4.5L6 12L2.5 8.5" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"/></svg>${f}</li>`).join('');
        }
    });

    // Update active state on language selector
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });
}

// Initialize on DOM load
document.addEventListener('DOMContentLoaded', () => {
    const lang = detectLanguage();
    setLanguage(lang);
});
